    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
                // TODO add your handling code here:
                counter++;
                if(counter<=sizeProcess){
               
                String name; float ar=0; float bur=0;   int prio=0;
                
                try{
                    name=NameProcess.getText();
                ar=Float.parseFloat(ArrivalProcess.getText());
                bur=Float.parseFloat(BurstProcess.getText());

                if(indexSelected==0){
                fc.addProcess(new Process(name,bur,ar));
                model= (DefaultTableModel) jTable1.getModel();
                 model.addRow(new Object[]{NameProcess.getText(),BurstProcess.getText(),ArrivalProcess.getText()});
                }
                else if(indexSelected==1){
                   sjn.addProcess(new Process(name,bur,ar));
                   model= (DefaultTableModel) jTable1.getModel();
                   model.addRow(new Object[]{NameProcess.getText(),BurstProcess.getText(),ArrivalProcess.getText()});
                }
                else if(indexSelected==2){
                   sjp.addProcess(new Process(name,bur,ar));
                   model= (DefaultTableModel) jTable1.getModel();
                    model.addRow(new Object[]{NameProcess.getText(),BurstProcess.getText(),ArrivalProcess.getText()});
                }                
                else if(indexSelected==3){
                    prio=Integer.parseInt(PriorityField.getText());
                    priN.addProcess(new Process(name,bur,ar,prio));
                    model= (DefaultTableModel) jTable1.getModel();
                    model.addRow(new Object[]{NameProcess.getText(),BurstProcess.getText(),ArrivalProcess.getText(),PriorityField.getText()});
                }
                else if(indexSelected==4){
                    prio=Integer.parseInt(PriorityField.getText());
                     priP.addProcess(new Process(name,bur,ar,prio));
                     model= (DefaultTableModel) jTable1.getModel();
                     model.addRow(new Object[]{NameProcess.getText(),BurstProcess.getText(),ArrivalProcess.getText(),PriorityField.getText()});
                }                
                else if(indexSelected==5){
                     RR.addProcess(new Process(name,bur,ar));
                       model= (DefaultTableModel) jTable1.getModel();
                      model.addRow(new Object[]{NameProcess.getText(),BurstProcess.getText(),ArrivalProcess.getText()});
                }

                } catch (NumberFormatException e){                   
                   errorFrame err=new errorFrame();
                    err.setVisible(true);
                    err.setLabelText("Enter number not string or empty value");
                }
                NameProcess.setText("");   ArrivalProcess.setText("");   BurstProcess.setText("");
                PriorityField.setText("");                
             }
                
                
                else{
                    errorFrame err=new errorFrame();
                    err.setVisible(true);
                    err.setLabelText("sorry you enter no of process exceeded you select");
                }
                
                                   
    }//GEN-LAST:event_AddButtonActionPerformed
