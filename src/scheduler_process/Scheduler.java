    public Scheduler(int msg,int noProcess) {
      
        sizeProcess=noProcess;
        if(msg==0) {fc.setNoProcess(noProcess); 
        }
        else if(msg==1) {sjn.setNoProcess(noProcess); 
        }
        else if(msg==2) {sjp.setNoProcess(noProcess); 
        }
        else if(msg==3) {priN.setNoProcess(noProcess);}
        else if(msg==4) {priP.setNoProcess(noProcess);}
        else if(msg==5) {RR.setNoProcess(noProcess);
        }
         indexSelected=msg;
           initComponents();
            setDefaultCloseOperation(Scheduler.DISPOSE_ON_CLOSE); 
         if(msg==3 || msg==4)
         {

             PriorityField.setVisible(true);
             PriorityLabel.setVisible(true);
             QuntumField.setVisible(false);
             QuantumLabel.setVisible(false);
         }
         else if(msg==5)
         {
              QuantumLabel.setVisible(true);
              QuntumField.setVisible(true);
              PriorityField.setVisible(false);
             PriorityLabel.setVisible(false);
              
         }
         else{
            QuantumLabel.setVisible(false);
            QuntumField.setVisible(false);
            PriorityField.setVisible(false);
            PriorityLabel.setVisible(false);   
         }
         disable();
    }

